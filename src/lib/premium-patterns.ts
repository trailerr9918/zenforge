// Helper: generate a Google Fonts URL from font families
export function buildFontsHref(fonts: string[]): string {
  const params = fonts.map(f => {
    const family = f.replace(/\s/g, '+');
    if (f.includes('Playfair')) return `family=${family}:ital,wght@1,400;1,500;1,600`;
    if (f.includes('Instrument')) return `family=${family}:ital,wght@0,400;0,700;1,400`;
    if (f.includes('Space Grotesk')) return `family=${family}:wght@400;500;600;700`;
    if (f.includes('Anton')) return `family=${family}`;
    return `family=${family}:wght@300;400;500;600;700`;
  });
  return `https://fonts.googleapis.com/css2?${params.join('&')}&display=swap`;
}

