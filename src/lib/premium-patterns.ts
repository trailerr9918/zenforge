/** Get all premium patterns for a given section type */
export function getPremiumPatterns(type: string): PremiumPattern[] {
  return PREMIUM_PATTERNS.filter(p => p.type === type);
}

/** Get the best premium pattern for a section type */
export function getBestPremiumPattern(type: string): PremiumPattern | null {
  const matching = getPremiumPatterns(type).sort((a, b) => b.fitness - a.fitness);
  return matching[0] || null;
}

/** Get all unique font families from premium patterns */
export function getAllPremiumFonts(): string[] {
  const fonts = new Set<string>();
  for (const p of PREMIUM_PATTERNS) {
    p.fonts.forEach(f => fonts.add(f));
  }
  return Array.from(fonts);
}

/** Get pattern by ID */
export function getPremiumPatternById(id: string): PremiumPattern | null {
  return PREMIUM_PATTERNS.find(p => p.id === id) || null;
}
